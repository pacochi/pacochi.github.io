const E = (name, attr = {}, children = []) => {

	const elm = Object.assign(document.createElement(name), attr);
	children.forEach(child => (child && elm.appendChild(child)));
	return elm;

};

const container = document.getElementById('comb'),
 ruby = document.getElementById('ruby'),
 body = document.getElementById('body'),
 result = document.getElementById('result'),
 copy = document.getElementById('copy');

const addRuby = e => {

	ruby.focus();

	const chr = e.target.textContent.slice(1);
	const start = ruby.selectionStart,
	 end = ruby.selectionEnd,
	 val = [...ruby.value];

	val.splice(start, end - start, chr);
	ruby.value = val.join('');
	characters.selectedIndex = -1;

	combine();

};

const combine = e => {

	let txt = '';
	[...body.value].forEach((chr, i) => {

		const rchr = ruby.value[i] || '';
		txt += chr + (/\s/.test(rchr) ? '' : rchr);

	});
	result.value = txt;

};

ruby.addEventListener('change', combine, false);
body.addEventListener('change', combine, false);

const buttons = characters.map(([code, name]) => {

	const chr = String.fromCodePoint(code);
	const button = E('button', {
		textContent: 'A' + chr,
		onclick: addRuby
	});
	container.append(E('label', {}, [button, E('span', { textContent: name })]));
	return button;

});

copy.addEventListener('click', e => navigator.clipboard.writeText(result.value));

combine();

